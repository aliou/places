class Place < ActiveRecord::Base
  ##############################################################################
  # Associations                                                               #
  ##############################################################################

  belongs_to :user

  ##############################################################################
  # Validations                                                                #
  ##############################################################################

  validates :name, presence: true
  validates :lat , presence: true
  validates :lng , presence: true

  ##############################################################################
  # Macros                                                                     #
  ##############################################################################

  acts_as_mappable :default_units => :kms
end
